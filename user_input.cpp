#include <iostream>

int main() {
    // Declare a variable to store the input number
    int number;

    // Prompt the user to enter a number
    std::cout << "Enter a number: ";

    // Read the number from the user
    std::cin >> number;

    // Print the number to the terminal
    std::cout << "You entered: " << number << std::endl;

    return 0;
}
